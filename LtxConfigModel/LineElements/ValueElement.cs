using System.Text;

namespace LtxConfigModel.LineElements;

/// <summary>
/// Container for one or more ValueItem instances representing the value side of a key/value pair.
/// The ValueElement holds outer padding that surrounds the entire value block and a list
/// of ValueItem objects for comma-separated tokens.
/// </summary>
public class ValueElement : LineElement
{
    private readonly List<ValueItem> _items = new();

    /// <summary>List of individual value tokens.</summary>
    public IReadOnlyList<ValueItem> Items => _items;

    /// <summary>Create a single value element (no list).</summary>
    public ValueElement(string value, Padding itemPadding = default)
    {
        _items.Add(new ValueItem(value, itemPadding));
    }

    /// <summary>Create a value element from a sequence of strings using the same padding for each item.</summary>
    public ValueElement(IEnumerable<string> values, Padding itemPadding = default)
    {
        foreach (var v in values)
            _items.Add(new ValueItem(v, itemPadding));
    }

    /// <summary>Create a value element from pre-built ValueItem instances (full control over each item padding).
    /// The elementPadding controls padding around the entire value block.</summary>
    public ValueElement(IEnumerable<ValueItem> items, Padding elementPadding = default)
    {
        _items.AddRange(items);
        Padding = elementPadding;
    }

    // Mutation
    /// <summary>Add a new value token to the list.</summary>
    public ValueItem Add(string value, Padding padding = default)
    {
        var item = new ValueItem(value, padding);
        _items.Add(item);
        return item;
    }

    /// <summary>Remove a value token from the list.</summary>
    public void Remove(ValueItem item) => _items.Remove(item);

    // Accessors
    /// <summary>First value as a plain string (or empty string when no items exist).</summary>
    public string SingleValue => _items.Count > 0 ? _items[0].Value : string.Empty;

    /// <summary>All value tokens as plain strings (padding removed).</summary>
    public IEnumerable<string> Values => _items.Select(i => i.Value);

    /// <summary>Number of value items.</summary>
    public int ItemsCount => _items.Count;

    public override string Serialize()
    {
        if (_items.Count == 0)
            return Padding.LeftStr;

        var sb = new StringBuilder();

        // Outer padding around the entire value block
        sb.Append(Padding.LeftStr);

        for (var i = 0; i < _items.Count; i++)
        {
            sb.Append(_items[i].Serialize());
            if (i < _items.Count - 1)
                sb.Append(',');
        }

        sb.Append(Padding.RightStr);

        return sb.ToString();
    }
}





















































































































































































































































































































































































































































































































































































































































